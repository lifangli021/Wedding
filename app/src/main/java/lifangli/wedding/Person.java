public class Person {
    private String name;
    private String tel;
    private String x;
    private String y;
    private String classify;
    private String maanger;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }

    public String getClassify() {
        return classify;
    }

    public void setClassify(String classify) {
        this.classify = classify;
    }

    public String getMaanger() {
        return maanger;
    }

    public void setMaanger(String maanger) {
        this.maanger = maanger;
    }
}
